#Ejercicio: Sistema de gestión de estudiantes
#Descripción:
#Vamos a crear un pequeño sistema para gestionar información sobre estudiantes en una escuela. Tendrás que realizar varias tareas:

#Crear un diccionario de estudiantes, donde las claves son los nombres de los estudiantes y los valores son listas que contienen:

# Edad del estudiante
# Calificaciones del estudiante (una lista de números de calificaciones)
# Función para agregar un estudiante: Crear una función agregar_estudiante(nombre, edad, calificaciones) que permita agregar un estudiante al diccionario. Si el estudiante ya existe, debe mostrar un mensaje indicándolo.

# Función para calcular el promedio de calificaciones de un estudiante: Crear una función calcular_promedio(nombre) que devuelva el promedio de las calificaciones de un estudiante. Si el estudiante no existe, debe mostrar un mensaje indicándolo.

# Función para mostrar todos los estudiantes: Crear una función mostrar_estudiantes() que imprima todos los estudiantes en el diccionario, mostrando su nombre, edad y promedio de calificaciones.

# Función para buscar estudiantes por rango de edad: Crear una función buscar_por_edad(min_edad, max_edad) que busque a los estudiantes cuyo rango de edad esté entre min_edad y max_edad y los muestre.

# Requisitos:
# El diccionario debe permitir almacenar múltiples estudiantes, y cada estudiante debe tener un nombre, edad y una lista de calificaciones.
# La función de cálculo de promedio debe manejar correctamente el caso en que el estudiante no tenga calificaciones.
# Debes agregar al menos 3 estudiantes y probar todas las funciones mencionadas.


